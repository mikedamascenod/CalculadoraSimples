public class Operacoes {

    public float soma(float numA, float numB) {
        float resultado;
        resultado = numA + numB;
        return resultado;
    }

    public float subtrai(float numA, float numB) {
        float resultado;
        resultado = numA - numB;
        return resultado;
    }

    public float multiplica(float numA, float numB) {
        float resultado;
        resultado = numA * numB;
        return resultado;

    }

    public float dividir(float numA, float numB) {
        float resultado;
        resultado = numA / numB;
        return resultado;

    }
}
